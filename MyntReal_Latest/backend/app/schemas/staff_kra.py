class KRATemplateCreate(BaseModel):
    """Schema for creating KRA template"""
    kra_code: Optional[str] = Field(None, max_length=32, description="Auto-generated by server if not provided")
    title: str = Field(..., min_length=5, max_length=256)
    description: Optional[str] = None
    applicable_to_role: Optional[str] = Field(None, max_length=64)
    applicable_to_designation: Optional[str] = Field(None, max_length=128)
    frequency: FrequencyType
    frequency_config: Optional[Dict[str, Any]] = None
    estimated_time_minutes: Optional[int] = Field(None, ge=1, le=480)
    target_time: Optional[str] = Field(None, pattern=r'^\d{2}:\d{2}$', description="Target completion time in HH:MM format (24hr)")
    is_mandatory: bool = True
    
    model_config = {"use_enum_values": True}
    
    @model_validator(mode='after')
    def validate_frequency_config(self):
        """Validate frequency_config matches frequency type"""
        frequency = self.frequency
        v = self.frequency_config
        
        if not frequency:
            return self
        
        # ADHOC requires no config
        if frequency == FrequencyType.ADHOC or frequency == 'adhoc':
            if v and v.get('pattern') != 'adhoc':
                raise ValueError('Ad-hoc frequency should have pattern="adhoc" or no config')
            self.frequency_config = v or {'pattern': 'adhoc'}
            return self
        
        # All other frequencies require config
        if not v:
            raise ValueError(f'{frequency} frequency requires frequency_config')
        
        pattern = v.get('pattern')
        
        if frequency == FrequencyType.DAILY or frequency == 'daily':
            if pattern != 'daily':
                raise ValueError('Daily frequency must have pattern="daily"')
        
        elif frequency == FrequencyType.EVERY_N_DAYS or frequency == 'every_n_days':
            if pattern != 'every_n_days':
                raise ValueError('Every N days frequency must have pattern="every_n_days"')
            interval = v.get('interval')
            if not interval or not (1 <= interval <= 90):
                raise ValueError('Every N days requires interval between 1-90')
        
        elif frequency == FrequencyType.SELECTED_DAYS or frequency == 'selected_days':
            if pattern != 'selected_days':
                raise ValueError('Selected days frequency must have pattern="selected_days"')
            days = v.get('days', [])
            if not days or len(days) < 1:
                raise ValueError('Selected days requires at least 1 day')
            valid_days = {'monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday', 'sunday'}
            normalized_days = []
            for day in days:
                day_lower = day.lower()
                if day_lower not in valid_days:
                    raise ValueError(f'Invalid day: {day}')
                normalized_days.append(day_lower)
            v['days'] = list(set(normalized_days))
        
        elif frequency == FrequencyType.EVERY_15_DAYS or frequency == 'every_15_days':
            if pattern != 'every_15_days':
                raise ValueError('Every 15 days frequency must have pattern="every_15_days"')
        
        elif frequency == FrequencyType.MONTHLY or frequency == 'monthly':
            if pattern != 'monthly':
                raise ValueError('Monthly frequency must have pattern="monthly"')
            day_of_month = v.get('day_of_month')
            if not day_of_month or not (1 <= day_of_month <= 31):
                raise ValueError('Monthly requires day_of_month between 1-31')
        
        elif frequency == FrequencyType.CUSTOM or frequency == 'custom':
            if pattern != 'custom':
                raise ValueError('Custom frequency must have pattern="custom"')
            dates = v.get('dates', [])
            if not dates:
                raise ValueError('Custom frequency requires dates array')
            v['dates'] = sorted(list(set(dates)))
        
        return self


class KRATemplateUpdate(BaseModel):
    """Schema for updating KRA template"""
    title: Optional[str] = Field(None, min_length=5, max_length=256)
    description: Optional[str] = None
    applicable_to_role: Optional[str] = Field(None, max_length=64)
    applicable_to_designation: Optional[str] = Field(None, max_length=128)
    frequency: Optional[FrequencyType] = None
    frequency_config: Optional[Dict[str, Any]] = None
    estimated_time_minutes: Optional[int] = Field(None, ge=1, le=480)
    target_time: Optional[str] = Field(None, pattern=r'^\d{2}:\d{2}$', description="Target completion time in HH:MM format (24hr)")
    is_mandatory: Optional[bool] = None
    status: Optional[RecordStatus] = None
    
    model_config = {"use_enum_values": True}
    
    @model_validator(mode='after')
    def validate_frequency_consistency(self):
        """Ensure frequency and frequency_config are consistent when updating"""
        frequency = self.frequency
        frequency_config = self.frequency_config
        
        if frequency and frequency_config:
            pattern = frequency_config.get('pattern')
            
            if (frequency == FrequencyType.DAILY or frequency == 'daily') and pattern != 'daily':
                raise ValueError('Daily frequency must have pattern="daily"')
            elif (frequency == FrequencyType.EVERY_N_DAYS or frequency == 'every_n_days') and pattern != 'every_n_days':
                raise ValueError('Every N days must have pattern="every_n_days"')
            elif (frequency == FrequencyType.SELECTED_DAYS or frequency == 'selected_days') and pattern != 'selected_days':
                raise ValueError('Selected days must have pattern="selected_days"')
            elif (frequency == FrequencyType.EVERY_15_DAYS or frequency == 'every_15_days') and pattern != 'every_15_days':
                raise ValueError('Every 15 days must have pattern="every_15_days"')
            elif (frequency == FrequencyType.MONTHLY or frequency == 'monthly') and pattern != 'monthly':
                raise ValueError('Monthly must have pattern="monthly"')
            elif (frequency == FrequencyType.CUSTOM or frequency == 'custom') and pattern != 'custom':
                raise ValueError('Custom must have pattern="custom"')
            elif (frequency == FrequencyType.ADHOC or frequency == 'adhoc') and pattern != 'adhoc':
                raise ValueError('Ad-hoc must have pattern="adhoc"')
        
        return self



class FrequencyConfigBase(BaseModel):
    """Base frequency configuration"""
    pattern: str
    
    model_config = {"extra": "forbid"}  # Reject unknown fields for DC Protocol compliance


class DailyFrequencyConfig(FrequencyConfigBase):
    """Daily frequency - every working day"""
    pattern: str = "daily"


class EveryNDaysConfig(FrequencyConfigBase):
    """Every N days frequency"""
    pattern: str = "every_n_days"
    interval: int = Field(..., ge=1, le=90, description="Days interval (1-90)")


class SelectedDaysConfig(FrequencyConfigBase):
    """Selected days frequency - any combination of 7 days"""
    pattern: str = "selected_days"
    days: List[str] = Field(..., min_length=1, max_length=7)
    
    @field_validator('days')
    @classmethod
    def validate_days(cls, v):
        valid_days = {'monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday', 'sunday'}
        for day in v:
            if day.lower() not in valid_days:
                raise ValueError(f'Invalid day: {day}. Must be one of {valid_days}')
        return [day.lower() for day in v]


class MonthlyConfig(FrequencyConfigBase):
    """Monthly frequency - specific day of month"""
    pattern: str = "monthly"
    day_of_month: int = Field(..., ge=1, le=31, description="Day of month (1-31)")


class CustomConfig(FrequencyConfigBase):
    """Custom frequency - specific dates in month"""
    pattern: str = "custom"
    dates: List[int] = Field(..., min_length=1, description="Day numbers in month")
    
    @field_validator('dates')
    @classmethod
    def validate_dates(cls, v):
        for d in v:
            if not 1 <= d <= 31:
                raise ValueError(f'Invalid date: {d}. Must be between 1-31')
        return sorted(list(set(v)))  # Remove duplicates and sort


class Every15DaysConfig(FrequencyConfigBase):
    """Every 15 days frequency"""
    pattern: str = "every_15_days"


class AdhocConfig(FrequencyConfigBase):
    """Ad-hoc frequency - manual assignment only"""
    pattern: str = "adhoc"


